<p><b>27/01/07 - <a href="/blog/rick-stein-s-leek-cannelloni-nightmare">Leek Cannelloni</a></b>
<br />"May I suggest that rather than cannelloni, you use pancakes.  Try making a smooth white 
sauce, then add lots of cheese.  Slice yer leeks and pop 'em in the micro for 2 - 3 mins 
with a knob of butter - make sure the dish is covered to avoid splashing and the effort of 
cleaning the micro.  Mix the leeks with about  half the cheese sauce.  Get your pancake, 
spoon on some leek mixture, season it if you like then roll up the pancake  to look like a 
cannelloni.  Repeat with the rest of your pancakes, then put them in a baking dish.  
Cover the neatly arranged pancakes with the remainder of the sauce.  Sprinkle some 
crushed plain crisps on the top and bake for 30 mins or so gas mark 5 or 6.
This also works well with tuna, haddock and bacon, and loads of other things.  Let your 
creativity take over. Lemon Thyme improves the flavour of all these dishes - ask Rick. Enjoy."
<br /><i>Pappajim, e-mail</i>
</p>
